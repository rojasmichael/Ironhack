class Calculator



	def calculate_add(first_number, second_number)

		first_number + second_number 
	end

	def calculate_subtract(first_number, second_number)

		first_number - second_number

	end

	def calculate_multi(first_number, second_number)

		first_number * second_number

	end

	def calculate_divide(first_number, second_number)
		first_number / second_number
	end 
end

